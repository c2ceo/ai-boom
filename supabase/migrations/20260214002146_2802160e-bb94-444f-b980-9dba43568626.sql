-- Functions for incrementing/decrementing counts
CREATE OR REPLACE FUNCTION public.increment_count(table_name TEXT, column_name TEXT, row_id UUID, amount INTEGER DEFAULT 1)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  EXECUTE format('UPDATE %I SET %I = %I + $1 WHERE id = $2', table_name, column_name, column_name)
  USING amount, row_id;
END;
$$;
